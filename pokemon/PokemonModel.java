public class PokemonModel {
    private String playerName;
    private String selectedStarterCreature;

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public void setSelectedStarterCreature(String selectedStarterCreature) {
        this.selectedStarterCreature = selectedStarterCreature;
    }

    public String getPlayerName() {
        return playerName;
    }

    public String getSelectedStarterCreature() {
        return selectedStarterCreature;
    }
    
}
